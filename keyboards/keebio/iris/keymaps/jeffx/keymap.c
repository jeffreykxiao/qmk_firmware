// Unused RGB Controls
// RGB_TOG, RGB_HUI, RGB_SAI, RGB_VAI,
// RGB_MOD, RGB_HUD, RGB_SAD, RGB_VAD,

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case QWERTY:
      if (record->event.pressed) {
        set_single_persistent_default_layer(_QWERTY);
      }
      return false;
      break;
    case GAMING:
      if (record->event.pressed) {
        if (biton32(layer_state) != _GAMING) {
          layer_on(_GAMING);
        }
      } else {
        layer_off(_GAMING);
      }
      return false;
      break;
    case LOWER:
      if (record->event.pressed) {
        layer_on(_LOWER);
      } else {
        layer_off(_LOWER);
      }
      return false;
      break;
    case RAISE:
      if (record->event.pressed) {
        layer_on(_RAISE);
      } else {
        layer_off(_RAISE);
      }
      return false;
      break;
    case GFUNC:
      if (record->event.pressed) {
        layer_on(_GFUNC);
      } else {
        layer_off(_GFUNC);
      }
      return false;
      break;
    case FUNCTION:
      if (record->event.pressed) {
        layer_on(_FUNCTION);
      } else {
        layer_off(_FUNCTION);
      }
      return false;
      break;
  }
  return true;
}

void encoder_update_user(uint8_t index, bool clockwise) {
    if (index == 0) {
        switch(biton32(layer_state)) {
          case _LOWER:
            clockwise ? tap_code(KC_MS_WH_RIGHT) : tap_code(KC_MS_WH_LEFT);
            break;
          case _FUNCTION:
            clockwise ? tap_code(KC_MS_WH_UP) : tap_code(KC_MS_WH_DOWN);
            break;
          default:
            clockwise ? tap_code(KC_DOWN) : tap_code(KC_UP);
            break;
        }
    }
    else if (index == 1) {
        switch(biton32(layer_state)) {
          case _FUNCTION:
            clockwise ? tap_code(KC_RIGHT) : tap_code(KC_LEFT);
            break;
          default:
            clockwise ? tap_code(KC_VOLU) : tap_code(KC_VOLD);
            break;
      }  
    }
}

uint8_t previousLayer = _QWERTY;

uint32_t layer_state_set_user(uint32_t state) {
    uint8_t layer = biton32(state);
    switch (layer) {
        case _QWERTY:
            if (previousLayer == _GAMING) {
                rgblight_mode_noeeprom(RGBLIGHT_MODE_BREATHING + 1);
            }
            rgblight_sethsv_noeeprom(HSV_CYAN);
            //backlight_set(0);
        break;
        case _GAMING:
            rgblight_mode_noeeprom(RGBLIGHT_MODE_RAINBOW_MOOD + 1);
            //rgblight_sethsv_noeeprom(HSV_WHITE);
            //backlight_set(5);
        break;
        case _LOWER:
            //rgblight_mode_noeeprom(RGBLIGHT_MODE_BREATHING);
            rgblight_sethsv_noeeprom(HSV_GREEN);
            //backlight_set(0);
        break;
        case _RAISE:
            //rgblight_mode_noeeprom(RGBLIGHT_MODE_BREATHING);
            rgblight_sethsv_noeeprom(HSV_MAGENTA);
            //backlight_set(0);
        break;
        case _GFUNC:
            rgblight_mode_noeeprom(RGBLIGHT_MODE_RAINBOW_SWIRL + 4);
            //rgblight_sethsv_noeeprom(HSV_CYAN);
            //backlight_set(0);
        break;
        case _FUNCTION:
            //rgblight_mode_noeeprom(RGBLIGHT_MODE_KNIGHT + 1);
            rgblight_sethsv_noeeprom(HSV_GOLD);
            //backlight_set(0);
        break;
    }
    previousLayer = layer;
  return state;
}

void matrix_init_user (void) {
  // Always enable numlock on keyboard init (for numpad layer)
  if (!(host_keyboard_leds() & (1<<USB_LED_NUM_LOCK))) {
      register_code(KC_NUMLOCK);
      unregister_code(KC_NUMLOCK);
  }
}